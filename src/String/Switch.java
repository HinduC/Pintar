package String;

public class Switch {
	
		public static void main(String[] args) 
		{
			System.out.println("Hello World!");
			int a=20, b=20;
			int c=a+b;
			switch (c)
			{
			case 1:
				System.out.println("A value"+a);
			case 40:
				System.out.println("B value"+b);
				break;
			case 3:
				System.out.println("A value");
			case 4:
				System.out.println("B value");
					
				
				
				default:
				System.out.println("default");
			
		
			}
		

		}
	}

